// Linearly interpolate the 5 threshold values [Beginner..Elite] from a table at value x
export function interpolateThresholds(table, x) {
  if (x <= table[0][0]) return table[0].slice(1)
  const last = table[table.length - 1]
  if (x >= last[0]) return last.slice(1)
  for (let i = 0; i < table.length - 1; i++) {
    const row0 = table[i], row1 = table[i + 1]
    if (x >= row0[0] && x <= row1[0]) {
      const t = (x - row0[0]) / (row1[0] - row0[0])
      return row0.slice(1).map((v, idx) => v + t * (row1[idx + 1] - v))
    }
  }
  return last.slice(1)
}

// Blend the bodyweight-based and age-based thresholds by averaging them
export function blendedStrengthThresholds(lift, gender, age, bodyweight) {
  const data = STRENGTH_STANDARDS[lift]?.[gender]
  if (!data) return null
  const byAge = interpolateThresholds(data.age, age)
  const byBW = interpolateThresholds(data.bodyweight, bodyweight)
  return {
    byAge,
    byBodyweight: byBW,
    blended: byAge.map((v, i) => (v + byBW[i]) / 2),
  }
}

export function classifyStrengthLift(actual, thresholds) {
  if (actual < thresholds[0]) return 'Untrained'
  for (let i = STRENGTH_LEVELS.length - 1; i >= 0; i--) {
    if (actual >= thresholds[i]) return STRENGTH_LEVELS[i]
  }
  return 'Untrained'
}
